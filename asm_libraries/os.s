INT7:
	# log that an interrupt happened
	kpsh r2
	kpsh r3
	lui	 r2, 0x100
	lw	 r3, r2, 0
	addi r3, r3, 1
	sw   r3, r2, 0
	kpop r3
	kpop r2
	rfi  r7, r5

move_to_user_space:
	# address in r2
	# length of program in r3
	# target address in r4
	# return address in r7
	kpsh r7
move_to_user_space_loop:
	lw   r7, r2, 0
	addi r2, r2, 1
	sw   r7, r4, 0		
	addi r4, r4, 1
	addi r3, r3, -1
	bnz move_to_user_space_loop
	kpop r7
	jalr r0, r7

write_tilemap:
  	kpsh r6
  	movi r6 0x0038
  	movi r2 tilemap
  	movi r4 0x0F00
  	movi r5 tilemap_end
write_tilemap_loop:
  	lw   r3 r2 0
  	sw   r4 r3 0
  	addi r2 r2 1
  	cmp  r2 r5
  	bnz  write_tilemap_loop
  	kpop r6
  	jalr r0 r7

