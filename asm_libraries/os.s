	# os = true

	# the cpu starts in kernel mode
	# this code will copy code appended after 'start' 
	# to user space and run it in user mode
	
	# for large programs, you may need to 
	# change the value in line 15
	
	# set up mem map
	movi r6, 0x0020
	
	# load program into user space
	movi r2, start
	lui  r3, 1000 # size of program (in words)
	lui  r4, 0x8000
	movi r7, move_to_user_space
	jalr r7, r7

  movi r7, write_tilemap
  jalr r7, r7
	
	# enable interupts
	movi r7, 0xFF00
	or   r6, r7, r6
	
	# run user program
	rfe  r0

terminal_index:
  .fill 0x8000

PUTCHAR:
	# 10 = newline TODO: handle this

  	# overwrites cr2, cr3, cr4, cr5

  	# could be more efficient - 
  	# need add more support for relative addressing in assembler

  	# writes the character in r3
  	kpsh r6
  	addi r6 r0 0x0030 # set mem map to framebuffer, disable interrupts
  	movi r4 terminal_index
  	lw r5 r4 0	# r5 has the terminal_index
  	tocr r3, r3

	addi r2 r0 10 # check for newline
	cmp  r2 r3
	beq  PUTCHAR_newline

  	sw r3 r5 0
  	addi r5 r5 1	# increment terminal_index

  	# lines are 128 memory addresses long,
  	# but only the first 50 show up on screen
  	# so whenever terminal_index is 128n + 50
  	# we set it to 128(n + 1)
  	addi r2 r0 63
  	and  r2 r5 r2 # r2 is now index mod 64
  	addi r3 r0 50
  	cmp  r3 r2
  	bne  PUTCHAR_done
  	movi r2 78
  	add  r5 r5 r2 # skip to the next line

PUTCHAR_done:
  	sw r5 r4 0
  	kpop r6
  	rfe r7

PUTCHAR_newline:
	addi r2 r0 63
  	and  r2 r5 r2 # r2 is now index mod 64
	sub  r5 r5 r2
	movi r2 128
	add  r5 r5 r2
	jmp PUTCHAR_done

EXC_PRIV:
	movi r1, 0xEEEE
	sys MODE_HALT

EXIT:
	# user programs return result in r3
	# put it in cr1 for testing
	tocr r1, r3
	sys MODE_HALT

